package edu.handong.analysis.datamodel;

public class Course{



	private String courseName ;

	
	public void Course(){

		courseName ="";

	}

	public void Course(String name){

		courseName = name ;

	}

	public void setCourseName(String name){

		courseName = name;
	}


	public String getCourseName(){

		return courseName ;
	
	}




}
