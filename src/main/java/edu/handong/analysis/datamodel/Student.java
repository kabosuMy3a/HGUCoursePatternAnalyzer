package edu.handong.analysis.datamodel;

public class Student{


	private String studentName ;


	public void Student(){
		
		studentName ="";

	}


	public void Student(String name){

		studentName = name ;

	}

	public void setStudentName(String name){
	
		studentName = name;
	}

	public String getStudentName(){
	
		return studentName;

	}




}
